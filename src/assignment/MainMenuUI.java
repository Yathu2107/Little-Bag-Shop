    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
         // Get the selected bag type from the ComboBox
    String selectedBagType = FBagType.getSelectedItem().toString();
    
    // Create an instance of BagDetails to fetch the data
    BagDetails bagDetails = new BagDetails();
    
    // Clear existing table rows
    DefaultTableModel tblModel = (DefaultTableModel) BagTable.getModel();
    tblModel.setRowCount(0);

    // If "All Bags" is selected, reload all bags
    if (selectedBagType.equals("All Bags")) {
        loadBagDataToTable(); // Load all bags
        return;
    }

    // Fetch filtered bag data based on the selected bag type
    String[][] filteredBags = bagDetails.SearchBagsByBagType(selectedBagType);

    // Add filtered bags to the table
    for (String[] bag : filteredBags) {
        tblModel.addRow(bag); // Add the filtered bag data to the table
    }

    // If no results are found, show a message
    if (filteredBags.length == 0) {
        JOptionPane.showMessageDialog(rootPane, "No bags found for the selected bag type", "Message", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_SearchButtonActionPerformed

