            UserAppLogins UAL=new UserAppLogins();
            UAL.AddNewUser(newUser);

            // Code to load to data into a table
            String data[]={UserFirstNameTextBox.getText(),UserLastNameTextBox.getText(),UserTypeTextBox.getText(),UsernameTextBox.getText()};
            DefaultTableModel tblModel = (DefaultTableModel)DataTable.getModel();
