    private void AddProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddProductButtonActionPerformed
       // Create an object for bag
        Bag newBag = new Bag();
        if (BagIDTextBox.getText().isEmpty() ||
            BagNameTextBox.getText().isEmpty() ||
            BagTypeTextBox.getText().isEmpty() ||
            BagPriceTextBox.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please fill in all fields", "Message", JOptionPane.ERROR_MESSAGE);
        }
        else{
            //Set values to the user
            newBag.SetBagID(BagIDTextBox.getText());
            newBag.SetBagName(BagNameTextBox.getText());
            newBag.SetBagType(BagTypeTextBox.getText());
            // Convert price to double
            double price = Double.parseDouble(BagPriceTextBox.getText());
            newBag.SetBagPrice(price);
            
            //create an object for UserAppLogics class
            BagDetails BD=new BagDetails();
            BD.AddNewBag(newBag);
            
            // Code to load to data into a table
            String data[]={BagIDTextBox.getText(),BagNameTextBox.getText(),BagTypeTextBox.getText(),BagPriceTextBox.getText()};
            DefaultTableModel tblModel = (DefaultTableModel)BagTable.getModel();
            //Add String array data
            tblModel.addRow(data);
            
            // Clear the text fields after successfully adding the bag
            BagIDTextBox.setText("");
            BagNameTextBox.setText("");
            BagTypeTextBox.setText("");
            BagPriceTextBox.setText("");

            // Optionally, reset focus to the first field
            BagIDTextBox.requestFocus();
        }
    }//GEN-LAST:event_AddProductButtonActionPerformed

