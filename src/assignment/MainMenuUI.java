    private void loadUserDataToTable() {
    // Create a table model and clear any existing rows
    DefaultTableModel tblModel = (DefaultTableModel) DataTable.getModel();
    tblModel.setRowCount(0);

    // Create an instance of UserAppLogins to fetch the user data
    UserAppLogins userAppLogins = new UserAppLogins();
    String[][] users = userAppLogins.GetAllUsers();

    // Add each user's data as a row in the table
    for (String[] user : users) {
        String[] data = {user[0], user[1], user[2], user[3]}; // Skip the password field
        tblModel.addRow(data); // Add the user data to the table
    }
}
    
    private void loadBagDataToTable() {
    // Create a table model and clear any existing rows
    DefaultTableModel tblModel = (DefaultTableModel) BagTable.getModel();
    tblModel.setRowCount(0);

    // Create an instance of bagdetails to fetch the user data
    BagDetails bagdetails = new BagDetails();
    String[][] bags = bagdetails.GetAllbags();

    // Add each user's data as a row in the table
    for (String[] bag : bags) {
        String[] bagData = {bag[0], bag[1], bag[2], bag[3]};
        tblModel.addRow(bagData); // Add the bag data to the table
    }
}
    
